// Импортируйте модуль `events` и создайте экземпляр `EventEmitter`.

// Зарегистрируйте обработчик для события `event` с использованием метода `once`.

// Сгенерируйте событие `event` и убедитесь, что обработчик вызывается.

// Снова сгенерируйте событие `event` и убедитесь, что обработчик больше не вызывается, так как он был 
const EventEmitter = require('events'); // 1. Правильный импорт модуля
const emitter = new EventEmitter();    // 2. Создаём экземпляр

// 3. Регистрируем однократный обработчик
emitter.once('event', (data) => {
    console.log('вызван', data);
});

// 4. Первый вызов - сработает
console.log('Первый вызов:');
emitter.emit('event', 'Hello world');  // Выведет "вызван Hello world"

// 5. Второй вызов - НЕ сработает
console.log('Второй вызов:');
emitter.emit('event', 'Hello again!'); // Ничего не выведет