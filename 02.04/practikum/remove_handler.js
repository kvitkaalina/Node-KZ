// Определите функцию-обработчик, которая будет регистрироваться для события `event`.

// Зарегистрируйте этот обработчик для события `event`.

// Сгенерируйте событие `event` и убедитесь, что обработчик вызывается.

// Удалите зарегистрированный обработчик для события `event`.

// Снова сгенерируйте событие `event` и убедитесь, что обработчик больше не вызывается.

const EventEmitter = require('events'); // Импортируем правильный модуль 'events'
const emitter = new EventEmitter(); // Создаём экземпляр EventEmitter

const eventHandler = () => {
    console.log('вызван!');
};

// 4. Регистрируем обработчик для события 'event'
emitter.on('event', eventHandler);

// 5. Генерируем событие 'event' (обработчик должен сработать)
console.log('Первый вызов события:');
emitter.emit('event'); // Выведет: "Обработчик события вызван!"

// 6. Удаляем обработчик
emitter.removeListener('event', eventHandler);

// 7. Снова генерируем событие 'event' (обработчик не должен сработать)
console.log('Второй вызов события после удаления обработчика:');
emitter.emit('event'); // Ничего не выведет

